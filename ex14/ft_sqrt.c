int	ft_sqrt(int nb)
{
	int	aux;
	int	count;

	aux = 1;
	count = 0;
	while (nb > 0)
	{	
		nb -= aux;
		aux += 2;
		count++;
	}
	if (nb < 0)
		return (0);
	return (count);
}

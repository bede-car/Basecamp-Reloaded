void	ft_putchar(char c);

int	main(int argc, char *argv[])
{
	int	j;
	int	i;

	j = 1;
	while (argc > 1)
	{
		i = 0;
		while (argv[j][i])
		{
			ft_putchar(argv[j][i]);
			i++;
		}
		ft_putchar('\n');
		j++;
		argc--;
	}
	return (0);
}

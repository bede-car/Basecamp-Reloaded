int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	dif;

	i = 0;
	while (s1[i] || s2[i])
	{
		if (s1[i] == s2[i])
			i++;
		else
			return (dif = s1[i] - s2[i]);
	}
	return (0);
}
